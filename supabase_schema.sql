-- 1. Heroes Table
create table if not exists heroes (
    id bigint generated by default as identity primary key,
    nama_gambar text not null,
    gambar text not null,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null,
    updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 2. Beritas (News) Table
create table if not exists beritas (
    id bigint generated by default as identity primary key,
    judul text not null,
    isi text not null,
    gambar text,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null,
    updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 3. Edukasis (Education) Table
create table if not exists edukasis (
    id bigint generated by default as identity primary key,
    judul text not null,
    konten text not null,
    gambar text,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null,
    updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 4. Tims (Team Members) Table
create table if not exists tims (
    id bigint generated by default as identity primary key,
    nama text not null,
    jabatan text not null,
    deskripsi text,
    foto text,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null,
    updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 5. Visi Misi Table
create table if not exists visi_misis (
    id bigint generated by default as identity primary key,
    about text not null,
    visi text not null,
    misi text not null,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null,
    updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 6. Clients (Korban) Table
create table if not exists clients (
    id uuid default gen_random_uuid() primary key,
    nama_lengkap text not null,
    jenis_kelamin text not null check (jenis_kelamin in ('Laki-laki', 'Perempuan')),
    status_korban text not null check (status_korban in ('Disable', 'Tidak')),
    kategori_disable text,
    status text not null, -- Mahasiswa, Dosen, Tendik, Masyarakat, etc.
    sumber_informasi text,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null,
    updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 7. Reporters (Pelapor) Table
create table if not exists reporters (
    id uuid default gen_random_uuid() primary key,
    hubungan_pelapor_dengan_pelaku text not null,
    nama_lengkap text not null,
    tempat_lahir text not null,
    tanggal_lahir date not null,
    jenis_kelamin text not null check (jenis_kelamin in ('Laki-laki', 'Perempuan')),
    usia integer not null,
    status_pelapor text not null,
    no_telepon text not null,
    email text not null,
    alamat text not null,
    keterangan_tambahan text,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null,
    updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 8. Perpetrators (Pelaku) Table
create table if not exists perpetrators (
    id uuid default gen_random_uuid() primary key,
    hubungan_dengan_korban text not null,
    nama text not null,
    telepon text,
    jenis_kelamin text not null check (jenis_kelamin in ('Laki-laki', 'Perempuan')),
    keterangan text not null,
    upload_bukti jsonb, -- Stored as JSON array of URLs/Paths
    created_at timestamp with time zone default timezone('utc'::text, now()) not null,
    updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 9. Violences (Kekerasan) Table
create table if not exists violences (
    id uuid default gen_random_uuid() primary key,
    jenis_kekerasan jsonb not null, -- JSON array of types, e.g. ["Seksual", "Fisik"]
    bentuk_kekerasan jsonb not null, -- JSON array of specific forms
    lokasi_kejadian text not null,
    waktu_kejadian timestamp with time zone not null,
    deskripsi_kekerasan text not null,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null,
    updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 10. Violence Reports (Laporan Utama) Table
create table if not exists violence_reports (
    id uuid default gen_random_uuid() primary key,
    id_client uuid not null references clients(id) on delete cascade,
    id_reporter uuid not null references reporters(id) on delete cascade,
    id_perpetrator uuid not null references perpetrators(id) on delete cascade,
    id_violence uuid not null references violences(id) on delete cascade,
    status text default 'terlapor' not null check (status in ('terlapor', 'diproses', 'ditolak', 'selesai')),
    code varchar(30) not null unique, -- Format: PPKS-YYYY-XXXXXXXXXX
    created_at timestamp with time zone default timezone('utc'::text, now()) not null,
    updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Add indexes for better query performance
create index if not exists idx_reports_client on violence_reports(id_client);
create index if not exists idx_reports_reporter on violence_reports(id_reporter);
create index if not exists idx_reports_perpetrator on violence_reports(id_perpetrator);
create index if not exists idx_reports_violence on violence_reports(id_violence);
create index if not exists idx_reports_code on violence_reports(code);

