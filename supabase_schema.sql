create policy "Allow public read heroes" on heroes for select using (true);
create policy "Allow public read beritas" on beritas for select using (true);
create policy "Allow public read edukasis" on edukasis for select using (true);
create policy "Allow public read tims" on tims for select using (true);
create policy "Allow public read visi_misis" on visi_misis for select using (true);

-- 2. Anyone can insert client/reporter/perpetrator/violence/report (for reporting case)
create policy "Allow public insert clients" on clients for insert with check (true);
create policy "Allow public insert reporters" on reporters for insert with check (true);
create policy "Allow public insert perpetrators" on perpetrators for insert with check (true);
create policy "Allow public insert violences" on violences for insert with check (true);
create policy "Allow public insert reports" on violence_reports for insert with check (true);

-- 3. Only authenticated (admin) users can perform CRUD on content tables and full read/write on reports
create policy "Allow admin CRUD heroes" on heroes for all to authenticated using (true);
create policy "Allow admin CRUD beritas" on beritas for all to authenticated using (true);
create policy "Allow admin CRUD edukasis" on edukasis for all to authenticated using (true);
create policy "Allow admin CRUD tims" on tims for all to authenticated using (true);
create policy "Allow admin CRUD visi_misis" on visi_misis for all to authenticated using (true);

